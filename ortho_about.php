<h2 class="t_ortho mincho">他の矯正方法との比較</h2>
<p>眼鏡やコンタクトレンズ、レーシック手術など他の視力矯正法は、視力矯正効果はあるものの、近視進行の抑制効果はありません。オルソケラトロジー治療は近視進行を抑える有効な治療法としても注目されています。特に、角膜のやわらかい子供の方に効果が出やすく、早く始めるほど近視進行を抑制できると報告されています。</p>
