<h2 class="t_ortho mincho">オルソケラトロジーの原理</h2>
<p>近視を矯正するには、光の屈折を変える必要があります。眼鏡は目の外で屈折を変え、コンタクトレンズは目の表面で屈折を変えています。オルソケラトロジーは目の表面で屈折を変えるという点ではコンタクトレンズと同じですが、人間が本来持っている角膜という場所に変形を加えることで、屈折を変えます。(変形を加えているだけなので、やめると元に戻ります。)オルソケラトロジーレンズ(左下図のオレンジ色の平べったい形をしたものです。)を装用することにより、角膜の形状をやや平たん化させて光の屈折を変えます。(右下図のように目の表面(角膜)が変形します。)レンズをはずしても一定期間、角膜の形状を保つことができるため焦点は合ったままとなり、日中は十分な裸眼視力が維持されます。</p>

